public class UnsupportedOperationException
  extends java.lang.RuntimeException
{
public  UnsupportedOperationException() { throw new RuntimeException("Stub!"); }
public  UnsupportedOperationException(java.lang.String detailMessage) { throw new RuntimeException("Stub!"); }
public  UnsupportedOperationException(java.lang.String message, java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }
public  UnsupportedOperationException(java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }
}
