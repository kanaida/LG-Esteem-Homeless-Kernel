package java.util.concurrent;
public class RejectedExecutionException
  extends java.lang.RuntimeException
{
public  RejectedExecutionException() { throw new RuntimeException("Stub!"); }
public  RejectedExecutionException(java.lang.String message) { throw new RuntimeException("Stub!"); }
public  RejectedExecutionException(java.lang.String message, java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }
public  RejectedExecutionException(java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }
}
