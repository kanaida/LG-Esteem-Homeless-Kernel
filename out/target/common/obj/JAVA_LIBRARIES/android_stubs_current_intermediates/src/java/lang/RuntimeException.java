package java.lang;
public class RuntimeException
  extends java.lang.Exception
{
public  RuntimeException() { throw new RuntimeException("Stub!"); }
public  RuntimeException(java.lang.String detailMessage) { throw new RuntimeException("Stub!"); }
public  RuntimeException(java.lang.String detailMessage, java.lang.Throwable throwable) { throw new RuntimeException("Stub!"); }
public  RuntimeException(java.lang.Throwable throwable) { throw new RuntimeException("Stub!"); }
}
